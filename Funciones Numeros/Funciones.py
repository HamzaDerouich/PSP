#Importamos la libreria math

import math

numero = 10
decimal = 2.89

# Redonde el decimal 
print(round(decimal))

# Devuelve el valor absoluto 

print(abs(abs(decimal)))

# Math ceil -> redonde para arriba

print(math.ceil(decimal))

# Math floor -> redondea para abajo 

print(math.floor(decimal))

#Es un número 

print(math.isnan(decimal))

#Elevamos un número a una potencia 

print(math.pow(decimal, 10))

# Obtener la raiz cuadrada 

print(math.sqrt(decimal))